a = list(map(int, input().split()))
b = list(map(int, input().split()))

# ---- 함수 ------------------------------------------------------------------

def get_dis(a : list[int], b : list[int]) -> float :
    return ((b[0] - a[0]) ** 2 +(b[1] - a[1]) ** 2) ** 0.5

# ---- 함수 ------------------------------------------------------------------

print(f'{get_dis(a, b):.4f}')